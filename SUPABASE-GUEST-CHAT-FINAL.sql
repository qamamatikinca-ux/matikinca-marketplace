create function public.get_pro_job_analytics(
  p_job_id uuid,
  p_owner_key text
)
returns jsonb
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_job public.job_listings%rowtype;
  v_authorised boolean;
  v_result jsonb;
begin
  select *
  into v_job
  from public.job_listings
  where id = p_job_id;

  if not found then
    raise exception 'Listing not found';
  end if;

  if coalesce(v_job.package_type, 'standard') <> 'pro' then
    raise exception 'Pro analytics required';
  end if;

  v_authorised := (
    length(coalesce(p_owner_key, '')) > 20
    and coalesce(v_job.owner_key, '') = p_owner_key
  ) or (
    auth.uid() is not null
    and v_job.user_id = auth.uid()
  );

  if not v_authorised then
    raise exception 'Not authorised';
  end if;

  select jsonb_build_object(
    'total_views', coalesce(v_job.view_count, 0),
    'unique_viewers', (
      select count(distinct viewer_hash)
      from public.job_view_events
      where job_id = p_job_id
        and viewer_hash is not null
    ),
    'last_viewed_at', v_job.last_viewed_at,
    'daily_views', (
      select coalesce(
        jsonb_agg(
          jsonb_build_object(
            'label', to_char(day_local, 'Dy'),
            'count', view_total
          )
          order by day_local
        ),
        '[]'::jsonb
      )
      from (
        select
          day_local,
          (
            select count(*)
            from public.job_view_events e
            where e.job_id = p_job_id
              and e.viewed_at >= (day_local at time zone 'Africa/Johannesburg')
              and e.viewed_at < ((day_local + interval '1 day') at time zone 'Africa/Johannesburg')
          ) as view_total
        from generate_series(
          date_trunc('day', now() at time zone 'Africa/Johannesburg') - interval '6 days',
          date_trunc('day', now() at time zone 'Africa/Johannesburg'),
          interval '1 day'
        ) as days(day_local)
      ) daily
    ),
    'devices', (
      select coalesce(
        jsonb_agg(
          jsonb_build_object('label', device_type, 'count', total)
          order by total desc
        ),
        '[]'::jsonb
      )
      from (
        select coalesce(device_type, 'unknown') as device_type, count(*) as total
        from public.job_view_events
        where job_id = p_job_id
        group by coalesce(device_type, 'unknown')
      ) grouped_devices
    ),
    'sources', (
      select coalesce(
        jsonb_agg(
          jsonb_build_object('label', source, 'count', total)
          order by total desc
        ),
        '[]'::jsonb
      )
      from (
        select coalesce(source, 'direct') as source, count(*) as total
        from public.job_view_events
        where job_id = p_job_id
        group by coalesce(source, 'direct')
      ) grouped_sources
    ),
    'recent_viewers', (
      select coalesce(
        jsonb_agg(
          jsonb_build_object(
            'name', coalesce(nullif(trim(p.full_name), ''), 'LoadLink member'),
            'viewed_at', recent.viewed_at
          )
          order by recent.viewed_at desc
        ),
        '[]'::jsonb
      )
      from (
        select viewer_user_id, max(viewed_at) as viewed_at
        from public.job_view_events
        where job_id = p_job_id
          and viewer_user_id is not null
        group by viewer_user_id
        order by max(viewed_at) desc
        limit 12
      ) recent
      left join public.profiles p on p.id = recent.viewer_user_id
    )
  )
  into v_result;

  return v_result;
end;
$$;

revoke all on function public.get_pro_job_analytics(uuid, text) from public;
grant execute on function public.get_pro_job_analytics(uuid, text) to anon, authenticated;

