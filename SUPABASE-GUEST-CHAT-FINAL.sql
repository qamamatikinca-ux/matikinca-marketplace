create function public.get_my_conversations()
returns table (
  id uuid,
  listing_id uuid,
  listing_title text,
  other_user_id uuid,
  other_name text,
  other_phone text,
  last_message text,
  last_message_at timestamptz,
  unread_count bigint,
  other_last_seen timestamptz
)
language sql
security definer
set search_path = public, pg_temp
as $$
  select
    c.id,
    c.listing_id,
    c.listing_title,
    other_person.user_id,
    other_person.display_name,
    other_person.phone,
    case
      when nullif(trim(last_message.body), '') is not null then last_message.body
      when last_message.file_path is not null then 'Attachment'
      else null
    end as last_message,
    last_message.created_at,
    (
      select count(*)
      from public.chat_messages unread
      where unread.conversation_id = c.id
        and unread.sender_id <> auth.uid()
        and unread.created_at > me.last_read_at
    ) as unread_count,
    p.last_seen
  from public.chat_conversations c
  join public.chat_participants me
    on me.conversation_id = c.id
   and me.user_id = auth.uid()
  join public.chat_participants other_person
    on other_person.conversation_id = c.id
   and other_person.user_id <> auth.uid()
  left join public.profiles p
    on p.id = other_person.user_id
  left join lateral (
    select body, file_path, created_at
    from public.chat_messages
    where conversation_id = c.id
    order by created_at desc
    limit 1
  ) last_message on true
  order by coalesce(last_message.created_at, c.updated_at) desc;
$$;

