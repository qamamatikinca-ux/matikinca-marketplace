create function public.start_listing_conversation(
  p_listing_id uuid,
  p_other_user uuid,
  p_other_name text
)
returns uuid
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_conversation_id uuid;
  v_listing_title text;
  v_listing_owner uuid;
  v_listing_phone text;
  v_me_name text;
  v_me_phone text;
  v_other_name text;
  v_other_phone text;
begin
  if auth.uid() is null then
    raise exception 'Sign in required';
  end if;

  select
    title,
    user_id,
    contact_number
  into
    v_listing_title,
    v_listing_owner,
    v_listing_phone
  from public.job_listings
  where id = p_listing_id;

  if not found then
    raise exception 'Listing not found';
  end if;

  if v_listing_owner is null then
    raise exception 'This listing is not connected to a LoadLink messaging account';
  end if;

  if p_other_user is distinct from v_listing_owner then
    raise exception 'Invalid recipient';
  end if;

  if v_listing_owner = auth.uid() then
    raise exception 'You cannot start a conversation with yourself';
  end if;

  select c.id
  into v_conversation_id
  from public.chat_conversations c
  join public.chat_participants me
    on me.conversation_id = c.id
   and me.user_id = auth.uid()
  join public.chat_participants other_person
    on other_person.conversation_id = c.id
   and other_person.user_id = v_listing_owner
  where c.listing_id = p_listing_id
  limit 1;

  if v_conversation_id is null then
    insert into public.chat_conversations (listing_id, listing_title)
    values (p_listing_id, coalesce(v_listing_title, 'LoadLink listing'))
    returning id into v_conversation_id;

    select
      coalesce(nullif(trim(full_name), ''), 'LoadLink user'),
      phone
    into v_me_name, v_me_phone
    from public.profiles
    where id = auth.uid();

    select
      coalesce(nullif(trim(full_name), ''), nullif(trim(p_other_name), ''), 'LoadLink user'),
      coalesce(phone, v_listing_phone)
    into v_other_name, v_other_phone
    from public.profiles
    where id = v_listing_owner;

    v_me_name := coalesce(v_me_name, 'LoadLink user');
    v_other_name := coalesce(v_other_name, nullif(trim(p_other_name), ''), 'LoadLink user');
    v_other_phone := coalesce(v_other_phone, v_listing_phone);

    insert into public.chat_participants (
      conversation_id,
      user_id,
      display_name,
      phone
    )
    values
      (v_conversation_id, auth.uid(), v_me_name, v_me_phone),
      (v_conversation_id, v_listing_owner, v_other_name, v_other_phone)
    on conflict (conversation_id, user_id) do nothing;
  end if;

  return v_conversation_id;
end;
$$;

revoke all on function public.start_listing_conversation(uuid, uuid, text) from public;
grant execute on function public.start_listing_conversation(uuid, uuid, text) to authenticated;

