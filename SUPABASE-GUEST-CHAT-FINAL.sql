create function public.get_daily_message_remaining()
returns integer
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_plan text;
  v_used integer;
  v_day_start timestamptz;
begin
  if auth.uid() is null then
    return 0;
  end if;

  select subscription_plan
  into v_plan
  from public.profiles
  where id = auth.uid();

  if coalesce(v_plan, 'standard') = 'pro' then
    return 999999;
  end if;

  v_day_start := (
    date_trunc('day', now() at time zone 'Africa/Johannesburg')
    at time zone 'Africa/Johannesburg'
  );

  select count(*)
  into v_used
  from public.chat_messages
  where sender_id = auth.uid()
    and created_at >= v_day_start;

  return greatest(0, 50 - v_used);
end;
$$;

revoke all on function public.get_daily_message_remaining() from public;
grant execute on function public.get_daily_message_remaining() to authenticated;

create function public.send_chat_message(
  p_conversation_id uuid,
  p_body text,
  p_file_path text default null,
  p_file_name text default null,
  p_file_type text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_remaining integer;
  v_message_id uuid;
  v_expected_prefix text;
begin
  if auth.uid() is null then
    raise exception 'Sign in required';
  end if;

  if not public.is_chat_participant(p_conversation_id) then
    raise exception 'Not authorised';
  end if;

  v_remaining := public.get_daily_message_remaining();

  if v_remaining <= 0 then
    raise exception 'Daily message limit reached';
  end if;

  if char_length(coalesce(p_body, '')) > 4000 then
    raise exception 'Message is too long';
  end if;

  if coalesce(trim(p_body), '') = '' and p_file_path is null then
    raise exception 'Empty message';
  end if;

  if p_file_path is not null then
    v_expected_prefix := p_conversation_id::text || '/' || auth.uid()::text || '/';

    if left(p_file_path, length(v_expected_prefix)) <> v_expected_prefix then
      raise exception 'Invalid attachment path';
    end if;
  end if;

  insert into public.chat_messages (
    conversation_id,
    sender_id,
    body,
    file_path,
    file_name,
    file_type
  )
  values (
    p_conversation_id,
    auth.uid(),
    coalesce(trim(p_body), ''),
    p_file_path,
    p_file_name,
    p_file_type
  )
  returning id into v_message_id;

  update public.chat_conversations
  set updated_at = now()
  where id = p_conversation_id;

  update public.profiles
  set last_seen = now(),
      updated_at = now()
  where id = auth.uid();

  return jsonb_build_object(
    'id', v_message_id,
    'remaining', case
      when v_remaining >= 999999 then 999999
      else greatest(0, v_remaining - 1)
    end
  );
end;
$$;

