create function public.record_job_view(
  p_job_id uuid,
  p_viewer_key text,
  p_device_type text default 'unknown',
  p_source text default 'direct'
)
returns boolean
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  v_owner_key text;
  v_owner_user_id uuid;
  v_viewer_hash text;
  v_already_recorded boolean;
begin
  select owner_key, user_id
  into v_owner_key, v_owner_user_id
  from public.job_listings
  where id = p_job_id;

  if not found then
    return false;
  end if;

  if length(coalesce(p_viewer_key, '')) < 8 then
    return false;
  end if;

  -- Do not count the listing owner viewing their own listing.
  if coalesce(v_owner_key, '') = p_viewer_key
     or (auth.uid() is not null and auth.uid() = v_owner_user_id) then
    return false;
  end if;

  v_viewer_hash := encode(digest(p_viewer_key, 'sha256'), 'hex');

  select exists (
    select 1
    from public.job_view_events
    where job_id = p_job_id
      and viewer_hash = v_viewer_hash
      and viewed_at > now() - interval '30 minutes'
  )
  into v_already_recorded;

  if v_already_recorded then
    return false;
  end if;

  insert into public.job_view_events (
    job_id,
    viewer_hash,
    viewer_user_id,
    device_type,
    source,
    viewed_at
  )
  values (
    p_job_id,
    v_viewer_hash,
    auth.uid(),
    case
      when p_device_type in ('mobile', 'tablet', 'desktop') then p_device_type
      else 'unknown'
    end,
    case
      when p_source in ('direct', 'google', 'social', 'whatsapp', 'loadlink', 'referral') then p_source
      else 'other'
    end,
    now()
  );

  update public.job_listings
  set view_count = coalesce(view_count, 0) + 1,
      last_viewed_at = now()
  where id = p_job_id;

  return true;
end;
$$;

revoke all on function public.record_job_view(uuid, text, text, text) from public;
grant execute on function public.record_job_view(uuid, text, text, text) to anon, authenticated;

